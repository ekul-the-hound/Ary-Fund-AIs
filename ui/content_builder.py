def _coerce_chart_list(raw: Any) -> list[ChartSpec]:
    """Normalize whatever the caller passed into a list of ChartSpec."""
    if raw is None:
        return []
    if isinstance(raw, ChartSpec):
        return [raw]
    if isinstance(raw, dict):
        # Either a single chart described as a dict, or {label: spec}
        if "source" in raw or "title" in raw:
            return [_spec_from_dict(raw)]
        return [_spec_from_dict({"title": k, "source": v}) for k, v in raw.items()]
    if isinstance(raw, list):
        return [
            spec if isinstance(spec, ChartSpec) else _spec_from_dict(spec)
            for spec in raw
        ]
    return []


def _spec_from_dict(d: Any) -> ChartSpec:
    if not isinstance(d, dict):
        return ChartSpec(title="Untitled chart", source=d)
    return ChartSpec(
        title=str(d.get("title") or d.get("name") or "Chart"),
        source=d.get("source") if "source" in d else d.get("path") if "path" in d else d.get("data"),
        caption=d.get("caption"),
        label=d.get("label"),
    )


def _split_essay_blocks(text: str) -> list[str]:
    """Split essay text into renderable paragraph blocks.

    The essay generator emits prose with double newlines between
    paragraphs and occasional Markdown-ish bold/headings. We preserve
    the paragraph breaks (one Paragraph per block) so the renderer can
    page-break between them, and let _classify_block decide which style
    to apply.
    """
    raw_blocks = [b.strip() for b in text.replace("\r\n", "\n").split("\n\n")]
    return [b for b in raw_blocks if b]


def _classify_block(block: str) -> tuple[str, str]:
    """Decide which paragraph style to use for an essay block."""
    stripped = block.strip()
    # Markdown-style heading
    if stripped.startswith("### "):
        return "H3", stripped[4:].strip()
    if stripped.startswith("## "):
        return "H2", stripped[3:].strip()
    if stripped.startswith("# "):
        return "H1", stripped[2:].strip()
    # Bold-prefixed paragraph (common in our deterministic fallback essay)
    if stripped.startswith("**") and "**" in stripped[2:]:
        return "Body", stripped
    return "Body", stripped


