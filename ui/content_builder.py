def _table(
    data: list[list[Any]],
    theme: DocTheme,
    col_widths: Optional[list[float]] = None,
    header_row: bool = True,
) -> Flowable:
    """Standard report table: thin grid, alt row shading."""
    # Convert any cell-content non-strings via str(); allow Paragraph
    # objects to flow through untouched.
    rows = []
    for row in data:
        rows.append([
            cell if hasattr(cell, "wrap") else _cell_paragraph(cell, theme)
            for cell in row
        ])
    t = Table(rows, colWidths=col_widths, hAlign="LEFT", repeatRows=1 if header_row else 0)
    style = [
        ("VALIGN",     (0, 0), (-1, -1), "TOP"),
        ("FONTSIZE",   (0, 0), (-1, -1), theme.size_small),
        ("LEFTPADDING",  (0, 0), (-1, -1), 4),
        ("RIGHTPADDING", (0, 0), (-1, -1), 4),
        ("TOPPADDING",   (0, 0), (-1, -1), 3),
        ("BOTTOMPADDING",(0, 0), (-1, -1), 3),
        ("LINEBELOW",  (0, 0), (-1, 0), 0.5, theme.color_rule),
        ("LINEABOVE",  (0, 0), (-1, 0), 0.5, theme.color_rule),
        ("ROWBACKGROUNDS", (0, 1 if header_row else 0), (-1, -1),
            [colors.white, theme.color_table_bg_alt]),
    ]
    if header_row:
        style += [
            ("FONTNAME", (0, 0), (-1, 0), theme.font_body_bold),
            ("TEXTCOLOR", (0, 0), (-1, 0), theme.color_secondary),
        ]
    t.setStyle(TableStyle(style))
    return t


def _two_col_table(rows: list[tuple[str, Any]], theme: DocTheme) -> Flowable:
    """Tight two-column label/value table used for metrics."""
    return _table(
        [[label, value] for label, value in rows],
        theme,
        col_widths=[2.0 * inch, 4.0 * inch],
        header_row=False,
    )


def _meta_table(rows: list[tuple[str, str]], theme: DocTheme) -> Flowable:
    """Two-column metadata table, monospaced values for IDs / timestamps."""
    styles = theme.styles()
    body = [
        [Paragraph(f"<b>{_escape(k)}</b>", styles["Meta"]),
         Paragraph(_escape(v), styles["Mono"])]
        for k, v in rows
    ]
    t = Table(body, colWidths=[1.6 * inch, 4.4 * inch], hAlign="LEFT")
    t.setStyle(TableStyle([
        ("VALIGN", (0, 0), (-1, -1), "TOP"),
        ("LEFTPADDING",  (0, 0), (-1, -1), 0),
        ("RIGHTPADDING", (0, 0), (-1, -1), 0),
        ("TOPPADDING",   (0, 0), (-1, -1), 2),
        ("BOTTOMPADDING",(0, 0), (-1, -1), 2),
    ]))
    return t


def _badge_row(items: list[tuple[str, str]], theme: DocTheme) -> Flowable:
    """Horizontal row of label/value badges used in the executive summary."""
    styles = theme.styles()
    cells = []
    for label, value in items:
        cells.append(
            Paragraph(
                f"<font size='{theme.size_small}' color='{theme.color_muted.hexval()}'>"
                f"{_escape(label.upper())}</font><br/>"
                f"<font size='{theme.size_h2}' color='{theme.color_primary.hexval()}'><b>"
                f"{_escape(value)}</b></font>",
                styles["Body"],
            )
        )
    t = Table([cells], hAlign="LEFT")
    t.setStyle(TableStyle([
        ("VALIGN", (0, 0), (-1, -1), "TOP"),
        ("LEFTPADDING", (0, 0), (-1, -1), 0),
        ("RIGHTPADDING", (0, 0), (-1, -1), 18),
        ("TOPPADDING", (0, 0), (-1, -1), 0),
        ("BOTTOMPADDING", (0, 0), (-1, -1), 0),
    ]))
    return t


def _bulleted(items: list[str], theme: DocTheme) -> Flowable:
    """Bullet list rendered as one Paragraph per bullet so it can paginate."""
    styles = theme.styles()
    body = "<br/>".join(
        f"• {_escape(str(item))}" for item in items
    )
    return Paragraph(body, styles["Body"])


def _cell_paragraph(value: Any, theme: DocTheme) -> Flowable:
    """Render a table cell value as a Paragraph so long strings wrap."""
    styles = theme.styles()
    if value is None:
        return Paragraph("<i>—</i>", styles["BodyMuted"])
    return Paragraph(_escape(str(value)), styles["Body"])


def _placeholder(text: str, theme: DocTheme) -> Flowable:
    return Paragraph(_escape(text), theme.styles()["Placeholder"])


