def build_appendix(ctx: dict, theme: DocTheme) -> list[Flowable]:
    styles = theme.styles()
    out: list[Flowable] = [Paragraph("Appendix & Sources", styles["H1"])]

    # 1) Data sources / provenance
    out.append(Paragraph("Data sources", styles["H2"]))
    sources = ctx.get("sources") or ctx.get("provenance") or {}
    if isinstance(sources, dict) and sources:
        rows = []
        for k, v in sorted(sources.items()):
            rows.append((k, _truncate(str(v), 80)))
        out.append(_two_col_table(rows, theme))
    elif isinstance(sources, list) and sources:
        out.append(_bulleted([str(s) for s in sources], theme))
    else:
        out.append(_placeholder(
            "No source / provenance metadata recorded in this snapshot.",
            theme,
        ))

    # 2) Missing-data summary — what we couldn't render
    out.append(Paragraph("Coverage notes", styles["H2"]))
    notes = _coverage_notes(ctx)
    if notes:
        out.append(_bulleted(notes, theme))
    else:
        out.append(Paragraph(
            "All expected sections were present in this snapshot.",
            styles["Body"],
        ))

    # 3) Methodology pointer
    out.append(Paragraph("Methodology", styles["H2"]))
    out.append(Paragraph(
        "This memo combines: rule-based fundamental and macro risk scoring, "
        "a heuristic 1-year thesis generator, an LLM-authored narrative "
        "essay (or deterministic fallback when offline), and a curated "
        "set of chart artifacts. See the project README for details on "
        "each component and on how to tune thresholds in "
        "<font face='Courier'>config.RISK_THRESHOLDS</font> and "
        "<font face='Courier'>thesis_generator</font> weights.",
        styles["Body"],
    ))

    # 4) Renderer / report metadata footer
    out.append(Paragraph("Report metadata", styles["H2"]))
    meta_rows = [
        ("Snapshot ID",      str(ctx.get("snapshot_id") or "(none)")),
        ("Snapshot date",    str(ctx.get("snapshot_date") or ctx.get("as_of") or "n/a")),
        ("Generated at",     str(ctx.get("generated_at") or _now_iso())),
        ("Report version",   REPORT_VERSION),
        ("Renderer version", RENDERER_VERSION),
        ("Output filename",  str(ctx.get("output_filename") or "(set by caller)")),
    ]
    out.append(_meta_table(meta_rows, theme))
    return out


def _coverage_notes(ctx: dict) -> list[str]:
    """Build a list of human-readable notes about what was missing."""
    notes: list[str] = []
    checks = [
        ("thesis essay",   bool((ctx.get("essay") or {}).get("text") if isinstance(ctx.get("essay"), dict) else False)),
        ("thesis summary", bool((ctx.get("thesis") or {}).get("summary"))),
        ("risk summary",   bool(ctx.get("risk") or ctx.get("risk_flags"))),
        ("metrics",        bool(ctx.get("metrics") or ctx.get("key_metrics"))),
        ("filings",        bool(ctx.get("filings") or ctx.get("filings_summary"))),
        ("macro",          bool(ctx.get("macro"))),
        ("charts",         bool(ctx.get("charts"))),
    ]
    for label, present in checks:
        if not present:
            notes.append(f"Section '{label}' was missing from the snapshot.")
    # Note explicit fallbacks the user should know about
    essay = ctx.get("essay")
    if isinstance(essay, dict) and essay.get("fallback"):
        notes.append("Thesis essay was generated by the deterministic fallback writer (LLM unavailable).")
    return notes


