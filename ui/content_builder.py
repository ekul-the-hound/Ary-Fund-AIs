def build_executive_summary(ctx: dict, theme: DocTheme) -> list[Flowable]:
    styles = theme.styles()
    thesis = ctx.get("thesis") or {}
    risk = ctx.get("risk") or ctx.get("risk_flags") or {}

    out: list[Flowable] = [Paragraph("Executive Summary", styles["H1"])]

    if not thesis and not risk:
        out.append(_placeholder(
            "No thesis or risk summary present in this snapshot. "
            "Verify that the agent chain completed before requesting a report.",
            theme,
        ))
        return out

    # Outlook badge row — outlook, direction, confidence, combined risk
    outlook = thesis.get("outlook") or "neutral"
    direction = thesis.get("price_direction") or "flat"
    conf = _as_float(thesis.get("confidence"))
    conf_pct = f"{conf:.0%}" if conf is not None else "n/a"
    combined_risk = (risk.get("levels") or {}).get("combined") or "n/a"

    badges = [
        ("Outlook",       outlook.upper()),
        ("Direction",     direction.replace("_", " ").upper()),
        ("Confidence",    conf_pct),
        ("Combined Risk", str(combined_risk).upper()),
    ]
    out.append(_badge_row(badges, theme))
    out.append(Spacer(1, 8))

    # Headline summary sentence
    summary = (
        thesis.get("summary")
        or thesis.get("rationale")
        or "(No summary provided.)"
    )
    out.append(Paragraph(
        f"<b>Summary.</b> {_escape(summary)}",
        styles["Body"],
    ))

    # Top risks + opportunities — capped lists keep the page calm
    risks = (thesis.get("key_risks") or [])[:5]
    opps = (thesis.get("key_opportunities") or [])[:5]
    if risks:
        out.append(Paragraph("<b>Key risks</b>", styles["H3"]))
        out.append(_bulleted(risks, theme))
    if opps:
        out.append(Paragraph("<b>Key opportunities</b>", styles["H3"]))
        out.append(_bulleted(opps, theme))

    return out


