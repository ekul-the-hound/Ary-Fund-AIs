def _first(d: Any, keys: tuple) -> Any:
    """Return d[key] for the first key in ``keys`` that has a value."""
    if not isinstance(d, dict):
        return None
    for k in keys:
        v = d.get(k)
        if v is not None and v != "":
            return v
    return None


def _as_float(x: Any) -> Optional[float]:
    try:
        if x is None or isinstance(x, bool):
            return None
        return float(x)
    except (TypeError, ValueError):
        return None


def _pct(x: Any) -> Optional[str]:
    """Format a fraction or percent as 'X.X%' — heuristic on magnitude."""
    f = _as_float(x)
    if f is None:
        return None
    # If the value is plausibly already in percent units (|v| > 1.5)
    # we leave it alone; otherwise treat as fraction
    if abs(f) > 1.5:
        return f"{f:.2f}%"
    return f"{f * 100:.2f}%"


def _money(x: Any) -> Optional[str]:
    f = _as_float(x)
    if f is None:
        return None
    sign = "-" if f < 0 else ""
    f = abs(f)
    if f >= 1e12:
        return f"{sign}${f / 1e12:.2f}T"
    if f >= 1e9:
        return f"{sign}${f / 1e9:.2f}B"
    if f >= 1e6:
        return f"{sign}${f / 1e6:.2f}M"
    if f >= 1e3:
        return f"{sign}${f / 1e3:.2f}K"
    return f"{sign}${f:,.2f}"


def _fmt_metric(value: Any) -> str:
    """Generic metric formatter used by tables."""
    if value is None:
        return "—"
    if isinstance(value, bool):
        return "yes" if value else "no"
    if isinstance(value, str):
        return value
    f = _as_float(value)
    if f is None:
        return str(value)
    # Heuristic: percentages and ratios get .2f; bigger numbers get money fmt
    if abs(f) >= 1e6:
        return _money(f) or str(f)
    if abs(f) < 10:
        return f"{f:.2f}"
    return f"{f:,.2f}"


def _truncate(s: str, n: int) -> str:
    if len(s) <= n:
        return s
    return s[: n - 1].rstrip() + "…"


def _now_iso() -> str:
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


