def build_title_page(ctx: dict, theme: DocTheme) -> list[Flowable]:
    styles = theme.styles()
    out: list[Flowable] = []

    # Top spacing pushes the title visually into the upper third
    out.append(Spacer(1, 1.2 * inch))

    title = ctx.get("report_title") or "Investment Memorandum"
    out.append(Paragraph(_escape(title), styles["Title"]))

    # Subtitle: ticker or portfolio scope, whichever applies
    subtitle = _scope_label(ctx)
    out.append(Paragraph(_escape(subtitle), styles["Subtitle"]))

    # Snapshot date + generated-at line
    snap_date = ctx.get("snapshot_date") or ctx.get("as_of") or "n/a"
    generated_at = ctx.get("generated_at") or _now_iso()
    snapshot_id = ctx.get("snapshot_id") or "(none)"
    analyst = ctx.get("analyst") or "Ary Fund Research System"

    meta_rows = [
        ("Snapshot as of", str(snap_date)),
        ("Generated at",   str(generated_at)),
        ("Snapshot ID",    str(snapshot_id)),
        ("Prepared by",    str(analyst)),
        ("Report version", REPORT_VERSION),
        ("Renderer",       RENDERER_VERSION),
    ]
    out.append(Spacer(1, 0.6 * inch))
    out.append(_meta_table(meta_rows, theme))

    # Disclaimer at the bottom
    out.append(Spacer(1, 1.4 * inch))
    disclaimer = (
        "<i>This memorandum is generated by an automated research system "
        "and is intended for internal review. Inputs are taken from public "
        "filings, market data, and macro indicators as of the snapshot "
        "date. Past performance is not indicative of future results. "
        "Nothing herein constitutes investment advice.</i>"
    )
    out.append(Paragraph(disclaimer, styles["Meta"]))
    out.append(PageBreak())
    return out


