def build_risk_commentary(ctx: dict, theme: DocTheme) -> list[Flowable]:
    styles = theme.styles()
    out: list[Flowable] = [Paragraph("Risk Commentary", styles["H1"])]

    risk = ctx.get("risk") or ctx.get("risk_flags") or {}
    if not risk:
        out.append(_placeholder(
            "No risk summary present in this snapshot.",
            theme,
        ))
        return out

    levels = risk.get("levels") or {}
    reasons = risk.get("reasons") or {}

    # Headline combined level
    combined = (levels.get("combined") or "n/a").upper()
    out.append(Paragraph(
        f"<b>Combined risk level:</b> {_escape(combined)}",
        styles["Body"],
    ))

    # Per-component breakdown — stable order
    for component in ("fundamental", "macro", "market", "agent"):
        lvl = levels.get(component)
        if lvl is None:
            continue
        out.append(Paragraph(
            f"{component.title()}: {_escape(str(lvl).upper())}",
            styles["H3"],
        ))
        comp_reasons = reasons.get(component) if isinstance(reasons, dict) else None
        if comp_reasons:
            real = [r for r in comp_reasons if r and r != "no data"]
            if real:
                out.append(_bulleted(real[:6], theme))
                continue
        out.append(Paragraph(
            "<i>No specific reasons recorded.</i>",
            styles["BodyMuted"],
        ))

    # If reasons came as a flat list (simpler caller), surface that too
    if isinstance(reasons, list) and reasons:
        real = [r for r in reasons if r and r != "no data"]
        if real:
            out.append(Paragraph("Additional risk notes", styles["H3"]))
            out.append(_bulleted(real[:8], theme))

    return out


