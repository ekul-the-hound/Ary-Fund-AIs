def build_supporting_context(ctx: dict, theme: DocTheme) -> list[Flowable]:
    styles = theme.styles()
    out: list[Flowable] = [Paragraph("Supporting Context", styles["H1"])]

    rendered_any = False

    # ---- Filings summary ---------------------------------------------------
    filings_section = _build_filings_section(ctx, theme)
    if filings_section:
        out.extend(filings_section)
        rendered_any = True

    # ---- Macro summary -----------------------------------------------------
    macro_section = _build_macro_section(ctx, theme)
    if macro_section:
        out.extend(macro_section)
        rendered_any = True

    # ---- Derived signals ---------------------------------------------------
    derived_section = _build_derived_section(ctx, theme)
    if derived_section:
        out.extend(derived_section)
        rendered_any = True

    # ---- Sentiment / geo (only if present) --------------------------------
    extra_section = _build_extra_context_section(ctx, theme)
    if extra_section:
        out.extend(extra_section)
        rendered_any = True

    if not rendered_any:
        out.append(_placeholder(
            "No supporting context (filings / macro / derived signals) "
            "present in this snapshot.",
            theme,
        ))

    return out


def _build_filings_section(ctx: dict, theme: DocTheme) -> list[Flowable]:
    styles = theme.styles()
    filings = ctx.get("filings") or ctx.get("filings_summary")
    if not filings:
        return []
    out = [Paragraph("Recent Filings", styles["H2"])]

    # Two shapes: a list of dicts (pipeline.build_agent_context) OR a
    # year-keyed dict (filing_analyzer.summarize_filings_by_year).
    rows: list[tuple[str, str, str]] = []  # (date, type, snippet)

    if isinstance(filings, list):
        for f in filings[:8]:
            if not isinstance(f, dict):
                continue
            rows.append((
                str(f.get("filed_date") or f.get("date") or ""),
                str(f.get("filing_type") or f.get("form") or ""),
                _truncate(str(f.get("description") or f.get("summary") or ""), 160),
            ))
    elif isinstance(filings, dict):
        # Either {year: {form: text}} OR {"recent": [...], "red_flags": [...]}
        if "recent" in filings or "red_flags" in filings:
            recent = filings.get("recent") or []
            for f in recent[:8]:
                if isinstance(f, dict):
                    rows.append((
                        str(f.get("filed_date") or ""),
                        str(f.get("filing_type") or ""),
                        _truncate(str(f.get("description") or ""), 160),
                    ))
        else:
            for year in sorted(filings.keys(), reverse=True):
                forms = filings[year]
                if not isinstance(forms, dict):
                    continue
                for form, text in forms.items():
                    rows.append((str(year), str(form), _truncate(str(text), 160)))

    if not rows:
        out.append(Paragraph(
            "<i>Filings present in snapshot but no parseable rows.</i>",
            styles["BodyMuted"],
        ))
        return out

    # Stable sort by date descending so re-runs produce identical layout
    rows.sort(key=lambda r: r[0], reverse=True)
    table_data = [["Date", "Form", "Note"]] + rows[:8]
    out.append(_table(table_data, theme, col_widths=[1.0 * inch, 0.8 * inch, 4.6 * inch]))
    return out


def _build_macro_section(ctx: dict, theme: DocTheme) -> list[Flowable]:
    styles = theme.styles()
    macro = ctx.get("macro") or {}
    if not macro:
        return []
    out = [Paragraph("Macro Snapshot", styles["H2"])]

    interesting = [
        ("VIX",                   _first(macro, ("vix", "global.vix"))),
        ("2s10s spread",          _first(macro, ("yield_curve_spread", "yield_curve_2y10y", "global.yield_curve_2y10y"))),
        ("Yield curve inverted",  _first(macro, ("yield_curve_inverted",))),
        ("Recession probability", _pct(_first(macro, ("recession_probability", "global.recession_prob")))),
        ("HY OAS",                _first(macro, ("hy_oas", "global.hy_oas"))),
        ("Financial stress",      _first(macro, ("financial_stress", "global.financial_stress"))),
        ("CPI YoY",               _pct(_first(macro, ("cpi_yoy_pct", "cpi_yoy")))),
        ("Unemployment rate",     _pct(_first(macro, ("unemployment_rate",)))),
    ]
    rows = [(name, _fmt_metric(val)) for name, val in interesting if val is not None]
    if not rows:
        out.append(Paragraph(
            "<i>Macro dict present but no recognizable fields populated.</i>",
            styles["BodyMuted"],
        ))
        return out
    out.append(_two_col_table(rows, theme))
    return out


def _build_derived_section(ctx: dict, theme: DocTheme) -> list[Flowable]:
    styles = theme.styles()
    derived = ctx.get("derived") or ctx.get("signals") or {}
    if not derived:
        return []
    out = [Paragraph("Derived Signals", styles["H2"])]
    rows = [(k, _fmt_metric(v)) for k, v in sorted(derived.items()) if v is not None]
    if not rows:
        out.append(Paragraph(
            "<i>Derived signals present but empty.</i>",
            styles["BodyMuted"],
        ))
        return out
    out.append(_two_col_table(rows[:16], theme))
    return out


def _build_extra_context_section(ctx: dict, theme: DocTheme) -> list[Flowable]:
    """Sentiment + geopolitical, only if present in the snapshot."""
    styles = theme.styles()
    out: list[Flowable] = []
    for key, label in (("sentiment", "Sentiment"), ("geo", "Geopolitical Context")):
        section = ctx.get(key)
        if not section:
            continue
        out.append(Paragraph(label, styles["H2"]))
        if isinstance(section, str):
            out.append(Paragraph(_escape(section), styles["Body"]))
        elif isinstance(section, dict):
            rows = [(k, _fmt_metric(v)) for k, v in sorted(section.items())
                    if v is not None]
            if rows:
                out.append(_two_col_table(rows, theme))
        elif isinstance(section, list):
            out.append(_bulleted([str(x) for x in section[:8]], theme))
    return out


