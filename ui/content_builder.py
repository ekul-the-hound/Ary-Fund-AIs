def _escape(s: Any) -> str:
    """Escape characters ReportLab Paragraph treats as XML markup."""
    return (
        str(s)
        .replace("&", "&amp;")
        .replace("<", "&lt;")
        .replace(">", "&gt;")
    )


def _escape_with_markup(text: str) -> str:
    """Escape but preserve a small set of Markdown-ish constructs.

    Currently: **bold** → <b>bold</b>. Anything else is plain-escaped.
    We do the bold transform BEFORE escaping the result.
    """
    parts: list[str] = []
    rest = text
    while True:
        i = rest.find("**")
        if i < 0:
            parts.append(_escape(rest))
            break
        parts.append(_escape(rest[:i]))
        j = rest.find("**", i + 2)
        if j < 0:
            # Unbalanced — fall back to literal text from here
            parts.append(_escape(rest[i:]))
            break
        parts.append(f"<b>{_escape(rest[i + 2:j])}</b>")
        rest = rest[j + 2:]
    return "".join(parts)
