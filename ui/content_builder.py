"""
report/content_builder.py
=========================
Assembles report sections from the snapshot context.

The renderer iterates ``SECTION_ORDER`` from ``template.py`` and calls
the matching ``build_<name>(ctx)`` function here. Each builder returns
a list of ReportLab flowables that will be appended to the document
Story.

Rules every builder follows
---------------------------
1. **Source-driven.** Builders consume what's in the context dict. They
   do not call out to the data layer or recompute anything.
2. **Missing-data behavior.** If the input for a section is absent, the
   builder returns a section header plus a labeled placeholder. The
   section never silently disappears.
3. **Deterministic.** Builders sort/order content from input data only.
   No random sampling, no wall-clock-dependent branching.
4. **Bounded prose.** Long inputs (thesis essay, filings list) are
   rendered as separate paragraphs so the renderer can page-break
   cleanly between them.
"""
from __future__ import annotations

import logging
from datetime import datetime, timezone
from typing import Any, Optional

from reportlab.lib import colors
from reportlab.lib.units import inch
from reportlab.platypus import (
    Flowable,
    KeepTogether,
    PageBreak,
    Paragraph,
    Spacer,
    Table,
    TableStyle,
)

from .charts import ChartSpec, build_chart_flowables
from .template import DEFAULT_THEME, REPORT_VERSION, RENDERER_VERSION, DocTheme

logger = logging.getLogger(__name__)


# =============================================================================
# Public dispatch
# =============================================================================


def build_section(
    name: str, ctx: dict, theme: DocTheme = DEFAULT_THEME,
) -> list[Flowable]:
    """Dispatch a section name to its builder.

    Unknown section names produce a placeholder rather than crashing —
    that way a mis-edited ``SECTION_ORDER`` doesn't kill the run.
    """
    builder = {
        "title_page":         build_title_page,
        "executive_summary":  build_executive_summary,
        "thesis":             build_thesis,
        "key_metrics":        build_key_metrics,
        "charts":             build_charts,
        "risk_commentary":    build_risk_commentary,
        "supporting_context": build_supporting_context,
        "appendix":           build_appendix,
    }.get(name)

    if builder is None:
        styles = theme.styles()
        return [
            Paragraph(f"<b>Unknown section: {_escape(name)}</b>", styles["H2"]),
            Paragraph(
                "This section name is listed in SECTION_ORDER but has no "
                "matching builder in content_builder.py.",
                styles["Placeholder"],
            ),
        ]

    try:
        return builder(ctx, theme)
    except Exception as e:  # noqa: BLE001 — section failure must not kill the report
        logger.exception("content_builder | section %s raised", name)
        styles = theme.styles()
        return [
            Paragraph(_section_title(name), styles["H1"]),
            Paragraph(
                f"Section '{name}' failed to render: {_escape(str(e))}",
                styles["Placeholder"],
            ),
        ]


