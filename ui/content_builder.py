def build_key_metrics(ctx: dict, theme: DocTheme) -> list[Flowable]:
    styles = theme.styles()
    out: list[Flowable] = [Paragraph("Key Metrics", styles["H1"])]

    metrics = ctx.get("metrics") or ctx.get("key_metrics") or {}
    prices = ctx.get("prices") or {}
    if not metrics and not prices:
        out.append(_placeholder(
            "No metric snapshot present in this snapshot.",
            theme,
        ))
        return out

    # Curated, ordered view of the metrics every memo cares about.
    # Sources tried in priority order; first hit wins.
    groups = [
        ("Valuation", [
            ("P/E (trailing)",    _first(metrics, ("pe_ratio", "trailing_pe", "p_e"))),
            ("P/E (forward)",     _first(metrics, ("forward_pe", "forward_p_e"))),
            ("EV / EBITDA",       _first(metrics, ("ev_ebitda", "ev_to_ebitda"))),
            ("P / Sales",         _first(metrics, ("ps_ratio", "price_to_sales"))),
            ("Market cap",        _money(_first(prices, ("market_cap",))
                                         or _first(metrics, ("market_cap",)))),
        ]),
        ("Growth & Margins", [
            ("Revenue growth (YoY)", _pct(_first(metrics, ("revenue_growth", "revenue_growth_yoy")))),
            ("Gross margin",         _pct(_first(metrics, ("gross_margin",)))),
            ("Operating margin",     _pct(_first(metrics, ("operating_margin",)))),
            ("Net margin",           _pct(_first(metrics, ("net_margin",)))),
            ("FCF yield",            _pct(_first(metrics, ("fcf_yield", "free_cash_flow_yield")))),
        ]),
        ("Balance Sheet & Risk", [
            ("Debt / EBITDA",   _first(metrics, ("debt_ebitda", "debt_to_ebitda"))),
            ("Net debt / EBITDA", _first(metrics, ("net_debt_ebitda", "net_debt_to_ebitda"))),
            ("Interest coverage", _first(metrics, ("interest_coverage",))),
            ("Beta",              _first(metrics, ("beta",))),
            ("52w drawdown",      _pct(_first(metrics, ("drawdown", "max_drawdown")))),
        ]),
    ]

    rendered_groups = 0
    for group_name, rows in groups:
        # Filter rows that have at least one non-None value, so empty
        # sections (e.g. for a brand-new ticker with no fundamentals)
        # don't render as a wall of n/a.
        rows_clean = [
            (name, _fmt_metric(val)) for name, val in rows
            if val is not None
        ]
        if not rows_clean:
            continue
        rendered_groups += 1
        out.append(Paragraph(group_name, styles["H3"]))
        out.append(_two_col_table(rows_clean, theme))
        out.append(Spacer(1, 4))

    if rendered_groups == 0:
        # Belt-and-braces: a context with `metrics`/`prices` present but
        # none of the canonical fields populated still gets a clear note
        # rather than an empty section.
        out.append(_placeholder(
            "Metric snapshot present but no standard fields populated.",
            theme,
        ))

    return out


