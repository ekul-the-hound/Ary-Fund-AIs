def build_thesis(ctx: dict, theme: DocTheme) -> list[Flowable]:
    styles = theme.styles()
    essay = ctx.get("essay") or {}
    thesis = ctx.get("thesis") or {}

    out: list[Flowable] = [Paragraph("Investment Thesis", styles["H1"])]

    text = (essay.get("text") if isinstance(essay, dict) else None) or ""
    text = text.strip()
    if not text:
        # No essay → fall back to the structured thesis fields, if present
        rationale = thesis.get("rationale")
        if rationale:
            out.append(Paragraph(_escape(rationale), styles["Body"]))
        else:
            out.append(_placeholder(
                "Thesis essay unavailable in snapshot.",
                theme,
            ))
        return out

    # Render the essay as one Paragraph per non-empty line/block. This
    # gives the renderer page-break points between paragraphs, which is
    # crucial for long essays.
    blocks = _split_essay_blocks(text)
    for block in blocks:
        # Headings in markdown-ish form: ## Foo / **Foo.**
        style_name, body = _classify_block(block)
        out.append(Paragraph(_escape_with_markup(body), styles[style_name]))

    # If the essay was a deterministic fallback, note that openly
    if isinstance(essay, dict) and essay.get("fallback"):
        out.append(Paragraph(
            "<i>Note: this thesis essay was produced by the deterministic "
            "fallback writer because the LLM was unavailable. Numeric "
            "claims are taken verbatim from the structured inputs.</i>",
            styles["Meta"],
        ))

    return out


